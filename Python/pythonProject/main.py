ex004 = input('digite algo ai meu patrão')
print('é numérica? {}' .format(ex004.isnumeric()))
print('é alfabética? {}' .format(ex004.isalpha()))
print('é alphanumérica? {}' .format(ex004.isalnum()))
print('Está em maiúscula? {}' .format(ex004.isupper()))
print('Está em minúscula? {}' .format(ex004.islower()))
print('Está capitalizada? {}' .format(ex004.isascii()))
print('seu tipo é {}', type(ex004))


